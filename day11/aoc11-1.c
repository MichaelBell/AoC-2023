typedef struct {
    uint8_t x;
    uint8_t y;
} pos_t;

#define MAX_GALAXIES 1000
pos_t galaxies[MAX_GALAXIES];
int num_galaxies = 0;

#define MAX_COLS 255
uint8_t col_map[MAX_COLS] = {};

uint8_t width = 0;
uint8_t height;

int main()
{
    const char* ptr = data;

    uint8_t x = 0;
    uint8_t y = 0;
    bool empty_row = true;

    while (*ptr) {
        if (*ptr == '\n') {
            if (width == 0) {
                if (ptr - data > 255) fail();
                width = ptr - data;
            }
            if (empty_row) ++y;
            empty_row = true;
            x = 0;
            ++y;
            if (y == 0) fail();
        }
        else if (*ptr == '#') {
            galaxies[num_galaxies].x = x;
            galaxies[num_galaxies++].y = y;
            empty_row = false;
            col_map[x] = 1;
            ++x;
        }
        else {
            ++x;
        }
        ++ptr;
    }

    height = y;

    //printf("%d %d %d\n", width, height, num_galaxies);

    for (uint8_t i = 0, j = 0; j < width; ++i, ++j) {
        if (col_map[i] == 0) {
            ++width;
            if (width == 0) fail();
            ++j;
        }
        else {
            col_map[i] = j;
        }
    }

    for (int j = 0; j < num_galaxies; ++j) {
        galaxies[j].x = col_map[galaxies[j].x];
        //printf("%d %d\n", galaxies[j].x, galaxies[j].y);
    }

    int total = 0;
    for (int i = 0; i < num_galaxies - 1; ++i) {
        for (int j = i+1; j < num_galaxies; ++j) {
            total += abs(galaxies[i].x - galaxies[j].x) + abs(galaxies[i].y - galaxies[j].y);
        }
    }

    printf("\n%d\n", total);
 
    return 0;
}
