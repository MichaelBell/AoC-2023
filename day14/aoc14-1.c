#define MAX_COL 100
#define MAX_ROW 100
char arena[MAX_COL * MAX_ROW];

int main()
{
    const char* ptr = data;

    int total = 0;

    const char* start = ptr;
    while (*ptr++ != '\n');
    const int cols = ptr - 1 - start;
    ptr = start;

    int x = 0;
    int y = 0;
    while (*ptr) {
        if (*ptr == '\n') {
            x = 0;
            ++y;
            ++ptr;
            continue;
        }

        if (*ptr == 'O') {
            arena[x+cols*y] = '.';
            int oy = y;
            while (oy > 0 && arena[x+cols*(oy-1)] == '.') --oy;
            arena[x+cols*oy] = 'O';
        }
        else {
            arena[x+cols*y] = *ptr;
        }
        ++ptr;
        ++x;
    }

    for (int i = 0; i < y; ++i) {
        printf("%.*s\n", cols, &arena[i*cols]);

        int load = y-i;
        for (int j = 0; j < cols; ++j) {
            if (arena[i*cols + j] == 'O') total += load;
        }
    }    

    printf("\n%d\n", total);
 
    return 0;
}