#define MAX_EXPANDED_SET_LEN 105
char expanded_set[MAX_EXPANDED_SET_LEN];
int expanded_set_idx;

#define MAX_DAMAGED_SETS 32
int damaged[MAX_DAMAGED_SETS];
int num_damaged_sets;

#define MAX_CACHED_COMBS (MAX_EXPANDED_SET_LEN << 5)
int64_t cached_combs[MAX_CACHED_COMBS];

int64_t count_combinations(const char* set, int reqd_len, int next_idx);

int64_t count_combinations_cached(const char* set, int idx)
{
    int i = (set - expanded_set);
    if (i < 0 || i >= MAX_EXPANDED_SET_LEN) fail();
    i = (i << 5) | idx;
    if (cached_combs[i] != -1) return cached_combs[i];

    cached_combs[i] = count_combinations(set, damaged[idx], idx+1);
    return cached_combs[i];
}

int64_t count_combinations(const char* set, int reqd_len, int next_idx)
{
    if (reqd_len == damaged[next_idx-1]) {
        while (*set == '.') set++;
        if (*set == ' ') return 0;
    }
    else if (reqd_len != 0 && (*set == '.' || *set == ' ')) return 0;

    while (*set == '#') {
        if (--reqd_len < 0) return 0;
        ++set;
    }

    if (reqd_len == 0) {
        if (next_idx == num_damaged_sets) {
            const char* s = set;
            while (*s != ' ') {
                if (*s++ == '#') return 0;
            }
            return 1;
        }
        if (*set == ' ') return 0;
        
        return count_combinations_cached(set + 1, next_idx);
    }
    else if (*set == '?') {
        int64_t combs = count_combinations(set+1, reqd_len-1, next_idx);
        if (reqd_len == damaged[next_idx-1]) {
            combs += count_combinations_cached(set + 1, next_idx - 1);
        }
        return combs;
    }

    return 0;
}

